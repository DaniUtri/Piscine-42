#include <stdio.h>
#include <unistd.h>

void	ft_is_negative(int n)
{
	int	a[2];

	a[0] = 78;
	a[1] = 80;
	if (n < 0)
		write(1, &a[0], 1);
	else
		write(1, &a[1], 1);
}
