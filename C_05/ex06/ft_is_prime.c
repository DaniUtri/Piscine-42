int	ft_is_prime(int nb)
{
	int	i;

	i = 1;
	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (0);
	if (nb == 2 || nb == 2147483647)
		return (1);
	while (i * i < nb)
	{
		i++;
		if (nb % i == 0)
			return (0);
	}
	return (1);
}
