void	input_to_table_1(char n[6][6])
{
	int	i;
	int	c;
	int	j;

	i = 0;
	c = 0;
	while (i < 6)
	{
		j = 0;
		while (j < 6)
		{
			n[i][j] = 42;
			j++;
		}
		i++;
	}
}
