void	s_numbers_rows_1(char	n[6][6])
{
	int	i;
	int	c;

	c = 1;
	i = 0;
	while (++i < 5)
	{
		if (n[i][0] == 52)
		{
			c = 1;
			while (c < 5)
			{
				n[i][c] = c + 48;
				c++;
			}
		}
		if (n[i][5] == 52)
		{
			c = 0;
			while (c-- > -5)
			{
				n[i][5 + c] = -c + 48;
			}
		}
	}
}
