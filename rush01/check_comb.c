int	check_comb(char *str, int i, char n[6][6])
{
	int	j;
	int	i_;

	if (i == 1)
		return (1);
	i_ = 1;
	while (i_ < i)
	{
		j = 1;
		while (j < 5)
		{
			if (n[i_][j] == str[j - 1])
				return (0);
			j++;
		}
		i_++;
	}
	return (1);
}
