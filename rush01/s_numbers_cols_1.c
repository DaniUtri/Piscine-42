void	s_numbers_cols_1(char	n[6][6])
{
	int	j;
	int	c;

	c = 1;
	j = 0;
	while (++j < 5)
	{
		if (n[0][j] == 52)
		{
			c = 1;
			while (c < 5)
			{
				n[c][j] = c + 48;
				c++;
			}
		}
		if (n[5][j] == 52)
		{
			c = 0;
			while (--c > -5)
			{
				n[5 + c][j] = -c + 48;
			}
		}
	}
}
