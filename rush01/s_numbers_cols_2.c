void	s_numbers_cols_2(char	n[6][6])
{
	int	j;

	j = 0;
	while (++j < 5)
	{
		if (n[0][j] == 49)
			n[0 + 1][j] = 52;
		if (n[5][j] == 49)
			n[5 - 1][j] = 52;
		if (n[0][j] == 50 && n[5][j] == 51)
			n[2][j] = 52;
		if (n[0][j] == 51 && n[5][j] == 50)
			n[3][j] = 52;
		if (n[0][j] == 49 && n[5][j] == 50)
		{
			n[1][j] = 52;
			n[4][j] = 51;
		}
		if (n[0][j] == 50 && n[5][j] == 49)
		{
			n[1][j] = 51;
			n[4][j] = 52;
		}
	}
}
